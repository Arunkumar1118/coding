
public class aTernary {                                // Ternary operator using if else

	public static void main(String[] args) {
		
		int x = 6;
		int result = 0;
		
		if (x%2==0)
			result = 10;
		else 
			result = 20;
		
		System.out.println(result);

	}

} 

